package com.vcsos.ClassStudy.b_final;

public final class Animal {
//1.概述:最终的----------final
//2.使用:
//  a.修饰一个类
//  b.修饰一个方法
//  c.修饰一个局部变量
//  d.修饰一个成员变量
//  e.修饰一个对象
//
//3.怎么学final:只需要知道被final修饰之后特点是啥即可
    /*
    * 一.final修饰类
    1.格式:
      public final class 类名{}
    2.特点:被final修饰的类不能被继承
    *
    *
    *
二.final修饰方法
1.格式:
  修饰符 final 返回值类型 方法名(形参){
      方法体
      return 结果
  }
2.特点:
  被final修饰的方法,不能被重写

3.注意:
  final和abstract不能同时修饰一个方法

三.final修饰局部变量
1.格式:
  final 数据类型 变量名 = 值
2.特点:
  被final修饰的变量不能二次赋值

四.final修饰对象
1.格式:
  final 数据类型 对象名 = new 对象();
2.特点:
  被final修饰的对象,地址值不能改变,但是对象中的属性值可以改变

五.final修饰成员变量
1.格式:
  final 数据类型 变量名 = 值
2.特点:
  a.需要手动赋值
  b.不能二次赋值


  * */
}
